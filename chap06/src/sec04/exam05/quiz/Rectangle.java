package sec04.exam05.quiz;

public class Rectangle {
	
//	Rectangle 클래스의 멤버는 다음과 같습니다.
//	[필드]
//	가로(width): 정수, 세로(height): 정수
	
	int width;
	int height;
//	
//	[메소드]
//	area(): 직사각형의 넓이를 출력
//	length(): 직사각형의 둘레를 출력
	
	void area() {
		System.out.println(width * height);

	}
	
	void length() {
		System.out.println((width*2) + (height*2));
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	

}
